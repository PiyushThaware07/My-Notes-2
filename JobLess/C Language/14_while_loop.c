// while loop : first it will check the condition and if condition satisfy the code will run else code stop or exit from loop

#include <stdio.h>
int main()
{
    /* code */
    int i = 1;
    while (i <= 10)
    {
        printf("%d\n", i);
        i = i + 1; // increment
    }
    printf("Loop Ends Here");
    return 0;
}
