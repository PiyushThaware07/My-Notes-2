'''
Datatype : A particular kind of data item,which was define by its values it can take it is called datatype.

Types :
1) Float
2) Integer
3) Boolean
4) String

since "Python" is a dynamic language there is no need to declare the datatype.
'''

# Integer Datatype :
a = 10
print('Integer Datatype :',a)

# Float Datatype :
b = 34.54
print('Float Datatype :',b)

# Boolean Datatype :
c = True
print('Boolean Datatype :',c)

# String Datatype :
d = "Piyush"
print('String Datatype :',d)


